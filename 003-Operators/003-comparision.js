// ---------- Comparision ----------

let x = 10;
let y = "10";

console.log(x == y);    // true (loose equality - checks for value)
console.log(x != y);    // false
console.log(x === y);   // false (strict equality - checks for value as well as data-type)
console.log(x !== y);   // true
console.log(x < y);     // false
console.log(x <= y);    // true
console.log(x > y);     // false
console.log(x >= y);    // true